#Utilizando separadores e end


print("Olá, amigo")
print("A nossa rebeldia é o povo no poder!")
print("A", "nossa", "rebeldia", "é o povo no poder!", sep=" ") # cria um separador padrão
print("A", "nossa", "rebeldia", "é o povo no poder!", sep="-")
print("A nossa rebeldia é o povo no poder!", end="\n") # cria um fim padrão.
print("A nossa rebeldia é o povo no poder!", end="Fim")
